#!/usr/bin/env node
/**
 * Generates SQL seed migrations from TypeScript base recipe modules.
 * Run: npm run generate:base-recipe-seed
 */
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  breakfastBaseRecipes,
  dinnerBaseRecipes,
  inspirationBaseRecipes,
  lunchBaseRecipes,
  snackBaseRecipes,
} from '../supabase/seeds/base-recipes/index.ts';

const __dirname = dirname(fileURLToPath(import.meta.url));
const migrationsDir = join(__dirname, '..', 'supabase', 'migrations');

function escapeSql(value) {
  return value.replace(/'/g, "''");
}

function sqlArray(values) {
  if (!values.length) {
    return "array[]::text[]";
  }
  return `array[${values.map((v) => `'${escapeSql(v)}'`).join(', ')}]::text[]`;
}

function generateRecipeSql(recipe) {
  const lines = [];
  const imageUrl = recipe.image_url ? `'${escapeSql(recipe.image_url)}'` : 'null';
  const imageStatus = recipe.image_status ? `'${escapeSql(recipe.image_status)}'` : `'pending'`;
  const imagePrompt = recipe.image_prompt ? `'${escapeSql(recipe.image_prompt)}'` : 'null';
  const imageStorageProvider = recipe.image_storage_provider
    ? `'${escapeSql(recipe.image_storage_provider)}'`
    : `'cloudflare_r2'`;
  const imageStorageKey = recipe.image_storage_key
    ? `'${escapeSql(recipe.image_storage_key)}'`
    : 'null';

  lines.push(
    `insert into public.recipes (` +
      `id, user_id, title, description, prep_time_minutes, portions, tags, ` +
      `image_url, image_status, image_prompt, image_storage_provider, image_storage_key, ` +
      `is_base_recipe, meal_type, category, difficulty, cook_time_minutes, instructions` +
      `) values (` +
      `'${recipe.id}', null, '${escapeSql(recipe.title)}', '${escapeSql(recipe.description)}', ` +
      `${recipe.prep_time_minutes}, ${recipe.portions}, ${sqlArray(recipe.tags)}, ` +
      `${imageUrl}, ${imageStatus}, ${imagePrompt}, ${imageStorageProvider}, ${imageStorageKey}, ` +
      `true, '${recipe.meal_type}', '${escapeSql(recipe.category)}', '${recipe.difficulty}', ` +
      `${recipe.cook_time_minutes}, ${sqlArray(recipe.instructions)}` +
      `) on conflict (id) do nothing;`
  );

  recipe.ingredients.forEach((ingredient) => {
    if (!ingredient.id) {
      throw new Error(`Missing ingredient id for ${recipe.title}`);
    }
    const qty = ingredient.quantity ?? 'null';
    const unit = ingredient.unit ? `'${escapeSql(ingredient.unit)}'` : 'null';
    lines.push(
      `insert into public.recipe_ingredients (id, recipe_id, name, quantity, unit) values (` +
        `'${ingredient.id}', '${recipe.id}', '${escapeSql(ingredient.name)}', ${qty}, ${unit}` +
        `) on conflict (id) do nothing;`
    );
  });

  return lines.join('\n');
}

function generateMigrationFile(label, recipes, migrationNumber) {
  const header = `-- PantryFlow: seed base recipes — ${label}\n-- Generated by scripts/generate-base-recipe-seed.mjs\n\n`;
  const body = recipes.map(generateRecipeSql).join('\n\n');
  const filename = join(
    migrationsDir,
    `0${migrationNumber}_seed_base_recipes_${label.toLowerCase().replace(/\s+/g, '_')}.sql`
  );
  writeFileSync(filename, header + body + '\n');
  console.log(`Wrote ${filename} (${recipes.length} recipes)`);
}

generateMigrationFile('breakfast', breakfastBaseRecipes, 21);
generateMigrationFile('lunch', lunchBaseRecipes, 22);
generateMigrationFile('dinner', dinnerBaseRecipes, 23);
generateMigrationFile('snacks', snackBaseRecipes, 24);
generateMigrationFile('inspiration', inspirationBaseRecipes, 28);

console.log(
  `Total: ${breakfastBaseRecipes.length + lunchBaseRecipes.length + dinnerBaseRecipes.length + snackBaseRecipes.length + inspirationBaseRecipes.length} recipes`
);
