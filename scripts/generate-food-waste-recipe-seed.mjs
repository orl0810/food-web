#!/usr/bin/env node
/** Generates the Supabase migration for the food-waste base recipe collection. */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const sourcePath = join(root, 'src', 'assets', 'base_recipes_food_waste_collection.json');
const migrationPath = process.argv[2];

if (!migrationPath) {
  throw new Error('Usage: node scripts/generate-food-waste-recipe-seed.mjs <migration-path>');
}

const collection = JSON.parse(readFileSync(sourcePath, 'utf8'));

function literal(value, cast = '') {
  if (value === null || value === undefined) return 'null';
  if (typeof value === 'number') return String(value);
  if (typeof value === 'boolean') return value ? 'true' : 'false';
  if (Array.isArray(value)) {
    const items = value.map((item) => `'${String(item).replace(/'/g, "''")}'`).join(', ');
    return `array[${items}]::text[]`;
  }
  return `'${String(value).replace(/'/g, "''")}'${cast}`;
}

const recipeColumns = [
  'id', 'user_id', 'title', 'description', 'prep_time_minutes', 'cook_time_minutes',
  'portions', 'tags', 'instructions', 'rating', 'meal_type', 'category', 'difficulty',
  'is_base_recipe', 'base_recipe_id', 'image_status', 'image_prompt', 'image_version',
  'image_storage_provider', 'nutrition_calories', 'nutrition_fat_g',
  'nutrition_cholesterol_mg', 'nutrition_protein_g', 'nutrition_sugar_g',
  'nutrition_sodium_mg', 'nutrition_carbs_g', 'nutrition_fiber_g',
  'nutrition_calculated_at',
];

const sql = [
  '-- PantryFlow: seed food-smart base recipes from base_recipes_food_waste_collection.json',
  '-- Generated by scripts/generate-food-waste-recipe-seed.mjs',
  '',
  'begin;',
  '',
];

for (const entry of collection.recipes) {
  const recipe = entry.recipe;
  sql.push(
    `insert into public.recipes (${recipeColumns.join(', ')})`,
    `values (${recipeColumns.map((column) => literal(recipe[column])).join(', ')})`,
    'on conflict (id) do update set',
    recipeColumns
      .filter((column) => column !== 'id')
      .map((column) => `  ${column} = excluded.${column}`)
      .join(',\n') + ';',
    ''
  );

  for (const ingredient of entry.ingredients) {
    sql.push(
      'insert into public.recipe_ingredients (id, recipe_id, name, quantity, unit)',
      `values (${literal(ingredient.id)}, ${literal(ingredient.recipe_id)}, ${literal(ingredient.name)}, ${literal(ingredient.quantity)}, ${literal(ingredient.unit)})`,
      'on conflict (id) do update set',
      '  recipe_id = excluded.recipe_id,',
      '  name = excluded.name,',
      '  quantity = excluded.quantity,',
      '  unit = excluded.unit;',
      ''
    );
  }
}

sql.push('commit;', '');
writeFileSync(join(root, migrationPath), sql.join('\n'));
console.log(`Wrote ${migrationPath} (${collection.recipes.length} recipes)`);
